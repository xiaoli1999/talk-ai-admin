function reqBlock(t) {
  return `【每条要求】
- **贴合"我"的语气（最优先）**：参考【我（玩家）的人设】(若上面有给) + 对话里"我"之前怎么说话——性格、口吻、长还是短、冷还是热、俏皮还是正经、爱用啥语气词/口头禅、怎么自称——5 条都要像"同一个我"会发的；**档位味/范例若和我的人设/语气冲突，先顺我的**（我冷就给冷的、我话痨就给热闹的、我毒舌就别给软萌）；**连"推动剧情"那两条也按我的人设来推**——我冷就冷着推、社恐就笨拙地推、知性就有分寸地推，别为了"推进/亲密"硬撩硬撒娇。
- 像真人发微信那样**自然、以对话为主**、${t.voice}；长度约 30-60 字（我话短就别硬撑长，我话多就自然些）。
- **形式别固定**：可带 (小动作)/(神态)/一处贴此刻场景的小细节，也可以纯说话。**() 别总放句首**：可放句中、句尾，或先说话再补个动作，甚至这条带、那条不带——位置和有无都别整齐划一。
- **() 写得活、贴场景**：扣着此刻具体的物件/动作/对方刚说的话写，别老是泛泛的"戳/扯/盯"；要带就用真人随手那种短小生活化的（撇嘴/翻白眼/憋着笑/凑近瞅…），别写角色那种长句唯美描写。
- 别用"假装受伤博抱""我之前见过类似的"这类老套路；绝不写 ‹›〔〕｛｝＊——旁白 这些角色通道符号（那是角色台词格式，不是你发消息的样子）。
- 5 条彼此要有差异、别雷同；不给"嗯/好/继续"这种把天聊死的话（我惜字也要有信息量、别真死）。
- preview ≤10 字、勾人、且准确预告 text。`;
}

const BAN = '【禁止】JSON 以外任何文字、markdown 代码块、替角色说话、超出当前关系尺度的露骨升级。范例只示意味道与形式，按真实对话自填、别照抄意象。';

/**
 * @param {object} character { name, rawText }
 * @param {object} opts { tier:'1'..'5', userDraft, userPersona, userName }
 * @returns {string} idea 系统 prompt
 */
function buildIdeaSystemPrompt(character, opts = {}) {
  const t = TIER_IDEA[String(opts.tier)] || TIER_IDEA['1'];
  const el = edgeLabelOf(opts.tier);
  const scale = el ? `\n【当前关系尺度】此刻亲密约「${el}」——候选别擅自跳到远超此刻关系的露骨/激烈，贴着当前推进。` : '';
  /* 称谓规则与主对话一致：设了名字且 ≠"我" → 用名字；否则用「主控」（"我"字扰乱视角，禁用）。
     标题用全称（名字（主控）），行文引用用短称（光名字）——避免括号嵌套连排 */
  const pn = (opts.userName || '').trim();
  const pshort = (pn && pn !== '我') ? pn : '主控';
  const pwho = (pn && pn !== '我') ? `${pn}（主控）` : '主控';
  const persona = (opts.userPersona && String(opts.userPersona).trim())
    ? `\n\n# 玩家 · ${pwho}\n候选回复要模仿 ${pshort} 的语气：\n${String(opts.userPersona).trim()}` : '';
  const head = characterIntro(character) + persona;
  const REQ = reqBlock(t);
  const draft = opts.userDraft != null ? String(opts.userDraft).trim() : '';

  if (draft) { // 模式B（有草稿）
    return `${head}

## 灵感回复任务（配合「${t.name}」档 · 我打了草稿）
我（用户）想跟角色说点什么、已经打了草稿或大概意思，但不知道怎么说好。我想表达的是：
「${draft}」
请**基于我这个意思**给我 **5 条**能直接发出去的候选。**铁律：**
- **草稿是第一优先**：哪怕当前对话情境很强（正淋雨/查案/吵架…），5 条也都要把我草稿的核心意思带进去，别被情境带跑题、别丢了我真正想说的。
- **这 5 条是"我"(玩家)要发出去的话**——是我的立场和口吻，**不是角色说的话**；别写成角色的台词或情绪（例：我草稿是"想哄生气的她"，就给"我"哄她的话，别写成她撒娇求哄）。
- 保留原意的前提下换花样：直接 / 委婉 / 俏皮或走心 / 多带细节 / 精简 各一种，但都围着我的原意，并贴此刻对话和「${t.name}」氛围、也贴我的语气。${scale}

${REQ}

输出**严格 JSON 数组**（5 条，无 markdown、无多余文字），tone 写这条的"变化角度"（如 直接/委婉/俏皮/带细节/精简）：
${MODE_B_EXAMPLE}

${BAN}`;
  }

  // 模式A（无草稿）
  const exJson = '[\n' + t.ex.map(([p, x], i) => {
    const tone = i < 2 ? '紧扣主题' : i < 4 ? '推动剧情' : '发散';
    return `  {"tone":"${tone}","preview":"${p}","text":"${x}"}`;
  }).join(',\n') + '\n]';
  return `${head}

## 灵感回复任务（配合「${t.name}」档）
我（用户）正在和上面的角色聊天，此刻是「${t.name}」的氛围——${t.flavor}。我聊到这儿不知道怎么接了，给我 **5 条**我能直接发出去的候选回复，整体味道贴合这个档。${scale}

【固定配比·5 条】严格按此顺序与数量：
① 紧扣主题 ×2：${t.stick}（两条走不同角度，别重复）
② 推动剧情 ×2：${t.push}（两条不同方向）
③ 发散 ×1：${t.diverge}（转移话题但仍合此刻情境）

${REQ}

输出**严格 JSON 数组**（5 条，按上面①②③顺序，无 markdown 包裹、无任何多余文字）：
${exJson}

${BAN}`;
}

