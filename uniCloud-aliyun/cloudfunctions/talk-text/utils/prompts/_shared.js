/**
 * 把角色信息转成"沉浸引入语段"，并在末尾注入【擦边本轮档位 Lx】。
 * 生产走 rawText 路径：用户写的角色设定原文整段塞入，不解析结构化字段。
 * @param {object|string} character { name, rawText, edgeLv, userPersona }
 */
function characterIntro(character) {
  // 字符串：直接当原文用
  if (typeof character === 'string') {
    return `# 你的角色\n\n${character.trim()}`;
  }

  const lines = ['# 你的角色', ''];
  if (character.name) lines.push(`你将沉浸地扮演**${character.name}**。`);
  lines.push('');

  const sections = [];

  // 生产乱文本路径：rawText 整段直接塞，不解析结构化字段
  if (character.rawText) {
    sections.push(character.rawText.trim());
  }

  // 用户（主控）设定独立结构段：与角色段平级，给模型干净的"你是谁/你在跟谁说话"二元边界，
  // 防止把用户特征吸收成角色属性（此前拼在角色 rawText 末尾易混淆）。不传则不注入（兼容老客户端拼接式 prompt）。
  // 称谓规则：设了名字且 ≠"我" → 用名字；否则用「主控」——"我"字在角色视角的 prompt 里指代混乱，禁用。
  if (character.userPersona && String(character.userPersona).trim()) {
    const n = (character.userName || '').trim();
    const short = (n && n !== '我') ? n : '主控';
    const who = (n && n !== '我') ? `${n}（主控）` : '主控';
    sections.push(`\n# 对话对象 · ${who}\n与你对话的人，你的所有回应都朝向 ${short}：\n${String(character.userPersona).trim()}\n`);
  }

  // 【擦边本轮档位 Lx】只注入当前这一档定义；不传则不注入(=不起擦边)。
  if (character.edgeLv && EDGE_LEVEL_EXPLAIN[character.edgeLv]) {
    sections.push(`【擦边本轮档位】${character.edgeLv}：${EDGE_LEVEL_EXPLAIN[character.edgeLv]}`);
  }

  return [...lines, ...sections].join('\n');
}

